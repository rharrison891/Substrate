using Substrate.Generation.Core.Helpers;
using Substrate.Generation.Core.Nodes;

using System.Text;

namespace Substrate.Generation.Core.Documents
{
    internal sealed class IconPackDocument : DocumentBase
    {
        private readonly TypeKey _key;
        private readonly IconPackNode _iconPack;
        private readonly IEnumerable<string> _usings;

        public IconPackDocument(TypeKey key, IReadOnlyList<SubstrateNode> iconNodes)
            : base(DocumentHelpers.GetHintName(key))
        {
            _key = key;

            _usings = iconNodes
                .SelectMany(n => n.Usings)
                .OrderBy(u => u)
                .Where(u => u != "<global namespace>")
                .Distinct();

            _iconPack = iconNodes
                        .OfType<IconPackNode>()
                        .FirstOrDefault()
                        ?? throw new InvalidOperationException("IconPackDocument requires at least one IconPackNode.");
        }

        public override string Build()
        {
            var sb = new StringBuilder();

            sb.AppendLine("// <auto-generated />");
            sb.AppendLine("// Substrate.IconPack");
            sb.BlankLine();

            foreach (var u in _usings)
                sb.AppendLine($"using {u};");

            sb.Nullable();
            sb.BlankLine();

            if (!string.IsNullOrEmpty(_key.Namespace))
            {
                sb.AppendLine($"namespace {_key.Namespace};");
                sb.BlankLine();
            }

            // ðŸ‘‡ next: emit enum + mapper here
            sb.StartRegion("Enum");
            EmitIconsEnum(sb);
            sb.EndRegion();
            sb.BlankLine();
            sb.StartRegion("Map");
            EmitMap(sb);
            sb.EndRegion();
            sb.BlankLine();
            sb.StartRegion("Helpers");
            EmitHelpers(sb);
            sb.EndRegion();

            return sb.ToString();
        }

